package com.cdgit.loan.irm.bean;

public class AdjustOption {

    private String iraApplyId;
    private String adjustOrder;
    private String adjustTypeCd;
    private String adjustOptionDescription;
    private String remarks;
    private String checked;
	public String getIraApplyId() {
		return iraApplyId;
	}
	public void setIraApplyId(String iraApplyId) {
		this.iraApplyId = iraApplyId;
	}
	public String getAdjustOrder() {
		return adjustOrder;
	}
	public void setAdjustOrder(String adjustOrder) {
		this.adjustOrder = adjustOrder;
	}
	public String getAdjustTypeCd() {
		return adjustTypeCd;
	}
	public void setAdjustTypeCd(String adjustTypeCd) {
		this.adjustTypeCd = adjustTypeCd;
	}
	public String getAdjustOptionDescription() {
		return adjustOptionDescription;
	}
	public void setAdjustOptionDescription(String adjustOptionDescription) {
		this.adjustOptionDescription = adjustOptionDescription;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getChecked() {
		return checked;
	}
	public void setChecked(String checked) {
		this.checked = checked;
	}
    
    
}
