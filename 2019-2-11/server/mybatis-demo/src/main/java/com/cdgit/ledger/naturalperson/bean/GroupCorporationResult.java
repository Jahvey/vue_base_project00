package com.cdgit.ledger.naturalperson.bean;
/**集团客户查询*/
public class GroupCorporationResult {
	private String partyId;
	private String partyNum;
	private String partyName;
	private String groupManageWayCd;
	private String status;
	private String orgNum;
	private String userNum;
	public String getPartyId() {
		return partyId;
	}
	public void setPartyId(String partyId) {
		this.partyId = partyId;
	}
	public String getPartyNum() {
		return partyNum;
	}
	public void setPartyNum(String partyNum) {
		this.partyNum = partyNum;
	}
	public String getPartyName() {
		return partyName;
	}
	public void setPartyName(String partyName) {
		this.partyName = partyName;
	}
	public String getGroupManageWayCd() {
		return groupManageWayCd;
	}
	public void setGroupManageWayCd(String groupManageWayCd) {
		this.groupManageWayCd = groupManageWayCd;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getOrgNum() {
		return orgNum;
	}
	public void setOrgNum(String orgNum) {
		this.orgNum = orgNum;
	}
	public String getUserNum() {
		return userNum;
	}
	public void setUserNum(String userNum) {
		this.userNum = userNum;
	}
	
}
